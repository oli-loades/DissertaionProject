package project;

import java.util.Date;

/**
 *
 * @author Oli Loades
 */
public class CommitStat {

    private String name;
    private Date date;

    CommitStat(String name, Date date) {
        this.name = name;
        this.date = date;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

}
